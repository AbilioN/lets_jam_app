  }

  void _onMessageReceived(
    MessageReceived event,
    Emitter<ChatState> emit,
  ) {
    if (state is ChatConnected) {
      final currentState = state as ChatConnected;
      
      print('üîµ ChatBloc - Mensagem recebida: ${event.message.content}');
      print('üîµ ChatBloc - Chat ID: ${event.message.chatId}');
      print('üîµ ChatBloc - Remetente: ${event.message.senderId}');
      
      // Verificar se a mensagem √© para o chat atual
      if (event.message.chatId == currentState.chatId) {
        // Adicionar a nova mensagem √† lista existente
        final updatedMessages = List<chat_service.ChatMessage>.from(currentState.messages);
        
        // Verificar se a mensagem j√° existe (evitar duplicatas)
        final messageExists = updatedMessages.any((msg) => 
          msg.id == event.message.id || 
          (msg.content == event.message.content && 
           msg.senderId == event.message.senderId &&
           msg.createdAt.difference(event.message.createdAt).inSeconds.abs() < 5)
        );
        
        if (!messageExists) {
          updatedMessages.add(event.message);
          print('üü¢ ChatBloc - Mensagem adicionada ao chat: ${event.message.content}');
          
          // Emitir novo estado com a mensagem adicionada
          emit(ChatConnected(
            chatId: currentState.chatId,
            messages: updatedMessages,
            chats: currentState.chats,
          ));
        } else {
          print('üü° ChatBloc - Mensagem j√° existe, ignorando duplicata');
        }
      } else {
        print('üü° ChatBloc - Mensagem n√£o √© para este chat (${event.message.chatId} != ${currentState.chatId})');
      }
    } else {
      print('üü° ChatBloc - Estado n√£o √© ChatConnected, ignorando mensagem');
    }
  }

