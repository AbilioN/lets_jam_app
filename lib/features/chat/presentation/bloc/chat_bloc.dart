  Future<void> _onChatInitialized(
    ChatInitialized event,
    Emitter<ChatState> emit,
  ) async {
    emit(ChatLoading());
    
    try {
      // Se j√° estamos inscritos em outro chat, desinscrever primeiro
      if (_currentChatId != null && _currentChatId != event.chatId) {
        print('üü° ChatBloc - Mudando de chat: $_currentChatId -> ${event.chatId}');
        print('üü° ChatBloc - Desinscrevendo do canal anterior: chat.$_currentChatId');
        await pusher_service.PusherService.unsubscribeFromChat(_currentChatId!);
      }
      
      // Atualizar o chat ativo
      _currentChatId = event.chatId;
      
      // Configurar callbacks do PusherService para chat
      pusher_service.PusherService.onChatEvent = (chatId, eventType, data) {
        print('üîµ ChatBloc - Evento de chat recebido: $eventType para chat $chatId');
        print('üîµ ChatBloc - Dados do evento: $data');
        
        // Verificar se o evento √© para o chat ativo
        if (chatId == _currentChatId.toString()) {
          print('üü¢ ChatBloc - Evento √© para o chat ativo: $chatId');
          
          // Processar diferentes tipos de eventos
          switch (eventType) {
            case 'MessageSent':
              _processPusherMessage(chatId, data);
              break;
            case 'chat-message':
              _processPusherMessage(chatId, data);
              break;
            case 'user-joined':
              print('üü° ChatBloc - Usu√°rio entrou no chat: $data');
              break;
            case 'user-left':
              print('üü° ChatBloc - Usu√°rio saiu do chat: $data');
              break;
            default:
              print('üü° ChatBloc - Evento n√£o tratado: $eventType');
          }
        } else {
          print('üü° ChatBloc - Evento n√£o √© para o chat ativo (${chatId} != ${_currentChatId})');
        }
      };
      
      // Inicializar PusherService se necess√°rio
      if (event.chatId != null) {
        print('üü° ChatBloc - Inscrevendo no canal: chat.${event.chatId}');
        
        // Inscrever no canal de chat espec√≠fico
        await pusher_service.PusherService.subscribeToChat(event.chatId!);
        
        // Log do status ap√≥s inscri√ß√£o
        _logChannelStatus();
        
        // Carregar mensagens do chat via API
        final result = await _getChatMessagesUseCase(
          GetChatMessagesParams(chatId: event.chatId!),
        );
        
        result.fold(
          (failure) => emit(ChatError('Erro ao carregar mensagens: $failure')),
          (messagesResponse) {
            // Converter MessageModel para ChatMessage
            final messages = messagesResponse.messages.map((message) => 
              chat_service.ChatMessage(
                id: message.id,
                chatId: message.chatId,
                content: message.content,
                senderId: message.senderId,
                senderType: message.senderType,
                isRead: message.isRead,
                createdAt: DateTime.parse(message.createdAt),
              )
            ).toList();
            
            emit(ChatConnected(
              chatId: event.chatId,
              messages: messages,
              chats: [], // Lista vazia por enquanto
            ));
          },
        );
      } else {
        // Se n√£o h√° chatId, emitir estado conectado sem mensagens
        emit(ChatConnected(
          chatId: event.chatId,
          messages: [],
          chats: [],
        ));
      }
      
    } catch (e) {
      emit(ChatError('Erro ao carregar mensagens: $e'));
    }
  }

  Future<void> _onMessageSent(
    MessageSent event,
    Emitter<ChatState> emit,
  ) async {
    if (state is ChatConnected) {
      final currentState = state as ChatConnected;
      
      try {
        // N√ÉO emitir ChatLoading - manter o estado atual
        // emit(ChatLoading()); // ‚Üê REMOVIDO
        
