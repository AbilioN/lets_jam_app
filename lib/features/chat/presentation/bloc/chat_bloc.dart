  void _processPusherMessage(String chatId, dynamic data) {
    print('üîµ ChatBloc - Processando mensagem do Pusher para chat: $chatId');
    print('üîµ ChatBloc - Dados brutos: $data');
    print('üîµ ChatBloc - Tipo dos dados: ${data.runtimeType}');
    
    Map<String, dynamic> messageData;
    
    // Verificar se os dados s√£o uma String JSON e fazer parse
    if (data is String) {
      try {
        messageData = Map<String, dynamic>.from(json.decode(data));
        print('üü¢ ChatBloc - JSON parseado com sucesso: $messageData');
      } catch (e) {
        print('üî¥ ChatBloc - Erro ao fazer parse do JSON: $e');
        return;
      }
    } else if (data is Map<String, dynamic>) {
      messageData = data;
      print('üü¢ ChatBloc - Dados j√° s√£o Map: $messageData');
    } else {
      print('üî¥ ChatBloc - Tipo de dados n√£o suportado: ${data.runtimeType}');
      return;
    }
    
    final messageContent = messageData['content'] as String?;
    final senderId = messageData['sender_id'] as int?;
    final senderType = messageData['sender_type'] as String?;
    final createdAt = messageData['created_at'] as String?;

    print('üîµ ChatBloc - content: $messageContent');
    print('üîµ ChatBloc - sender_id: $senderId (tipo: ${senderId.runtimeType})');
    print('üîµ ChatBloc - sender_type: $senderType');
    print('üîµ ChatBloc - created_at: $createdAt');

    if (messageContent != null && senderId != null && senderType != null && createdAt != null) {
      final message = chat_service.ChatMessage(
        id: messageData['id'] as int? ?? DateTime.now().millisecondsSinceEpoch, // Usar ID do servidor se dispon√≠vel
        chatId: int.parse(chatId), // Converter String para int
        content: messageContent,
        senderId: senderId,
        senderType: senderType,
        isRead: messageData['is_read'] as bool? ?? false,
        createdAt: DateTime.parse(createdAt),
      );
      
      print('üü¢ ChatBloc - Mensagem criada com sucesso: ${message.content}');
      add(MessageReceived(message: message));
    } else {
      print('üî¥ ChatBloc - Dados de mensagem incompletos do Pusher: $messageData');
      print('üî¥ ChatBloc - content: $messageContent, sender_id: $senderId, sender_type: $senderType, created_at: $createdAt');
    }
  }

